    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        bagagem.setNomeDono(ComboNome.getSelectedItem().toString());
        bagagem.setCpfDono(ComboCpf.getText());
        bagagem.setPassaporteDono(ComboPassaporte.getText());
        bagagem.setPeso(peso.getText());
       
        try {
            file.saveBaggage(bagagem);
            bagagem = new Bagagem();
            file = new File();
            
            JOptionPane.showMessageDialog(this, "Bagagem Salva Com Sucesso");
            
            this.dispose();
            BaggageList baggageList = new BaggageList();
            baggageList.setVisible(true);
            listarBagagens();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "ERRO - Bagagem NÃ£o Foi Salva");
        }
        

        this.clearFilds();
      
    }//GEN-LAST:event_btnSaveActionPerformed

